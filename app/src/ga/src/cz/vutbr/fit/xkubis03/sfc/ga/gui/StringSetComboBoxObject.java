package cz.vutbr.fit.xkubis03.sfc.ga.gui;

public class StringSetComboBoxObject {

    protected String label;

    public StringSetComboBoxObject(String label) {
        this.label = label;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    @Override
    public String toString() {
        return label;
    }
}
